<h2 align="center">Sistem Pakar Diagnosis Penyakit Gigi Dengan Metode Forward Channing</h2>
<p align='Justify'>Sistem pakar diagonsa penyakit gigi menggunakan metode <i>Forward Channing</i> adalah sebuah aplikasi berbasis web yang mengadopsi pengetahuan pakar untuk mendiagnosis pneyakit gigi sesuai dengan pengetahuan pakar. Sistem pakar ini dapat mendiagnosis penyakit gigi anda. Sistem pakar ini dkembangkan dengan model tanya-jawab gejala yang anda alami dan sistem akan mendiagnosis penyakit gigi anda. Selain itu sistem pakar ini juga dilengkapi fitur cetak untuk mencetak hasil diagnosis anda.</p>
